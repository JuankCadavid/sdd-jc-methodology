#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

function printHelp() {
  console.log(`AKILI Test Report Parser

Usage:
  node parse_tests.js <input-results.json> [output-report.md]

Arguments:
  input-results.json  JSON output file generated by Jest/Vitest (using --json option)
  output-report.md    Optional path to write/append the markdown report (defaults to stdout)
`);
}

function parseTestJson(inputPath) {
  if (!fs.existsSync(inputPath)) {
    console.error(`ERROR: Input file not found: ${inputPath}`);
    process.exit(1);
  }

  let data;
  try {
    const raw = fs.readFileSync(inputPath, "utf8");
    data = JSON.parse(raw);
  } catch (e) {
    console.error(`ERROR: Failed to parse JSON: ${e.message}`);
    process.exit(1);
  }

  // extract overall metrics
  const total = data.numTotalTests || 0;
  const passed = data.numPassedTests || 0;
  const failed = data.numFailedTests || 0;
  const pending = data.numPendingTests || 0;
  const suites = data.numTotalTestSuites || 0;

  let markdown = `## Test Summary

- **Total Test Suites:** ${suites}
- **Total Test Cases:** ${total}
- **Passed:** ${passed}
- **Failed:** ${failed}
- **Skipped/Pending:** ${pending}

### Detailed Traceability Matrix

| Test Suite / File | Test Case | Status | Duration | Notes / Failure Messages |
| :--- | :--- | :--- | :--- | :--- |
`;

  const results = data.testResults || [];
  for (const suite of results) {
    const relativeSuitePath = path.relative(process.cwd(), suite.name || "unknown");
    const assertions = suite.assertionResults || [];

    if (assertions.length === 0 && suite.message) {
      // Suite-level failure or initialization failure
      markdown += `| \`${relativeSuitePath}\` | *Suite Failure* | 🔴 **FAILED** | - | \`${suite.message.replace(/\r?\n/g, " ").substring(0, 100)}...\` |\n`;
      continue;
    }

    for (const test of assertions) {
      const fullTitle = [...(test.ancestorTitles || []), test.title].join(" > ");
      let statusIndicator = "⚪ **PENDING**";
      if (test.status === "passed") {
        statusIndicator = "🟢 **PASSED**";
      } else if (test.status === "failed") {
        statusIndicator = "🔴 **FAILED**";
      }

      const duration = test.duration ? `${test.duration}ms` : "-";
      let failureNotes = "-";
      if (test.failureMessages && test.failureMessages.length > 0) {
        // Strip ANSI escape codes and clean up message
        failureNotes = test.failureMessages[0]
          .replace(/[\u001b\u009b][[()#;?]*(?:[0-9]{1,4}(?:;[0-9]{0,4})*)?[0-9A-ORZcf-nqry=><]/g, "")
          .replace(/\r?\n/g, " ")
          .substring(0, 120);
        failureNotes = `\`${failureNotes}...\``;
      }

      markdown += `| \`${relativeSuitePath}\` | ${fullTitle} | ${statusIndicator} | ${duration} | ${failureNotes} |\n`;
    }
  }

  return markdown;
}

function main() {
  const args = process.argv.slice(2);
  if (args.length === 0 || args.includes("--help") || args.includes("-h")) {
    printHelp();
    process.exit(0);
  }

  const inputPath = args[0];
  const outputPath = args[1];

  const parsedMarkdown = parseTestJson(inputPath);

  if (outputPath) {
    fs.writeFileSync(outputPath, parsedMarkdown, "utf8");
    console.log(`Successfully generated markdown report at: ${outputPath}`);
  } else {
    console.log(parsedMarkdown);
  }
}

main();
